#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dup;
	int		i;

	dup = (char*)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
